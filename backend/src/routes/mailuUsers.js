router.post("/duplicates", async (req, res, next) => {
  try {
    validateMailuConfig(req.body);
    const users = await listMailuUsers(req.body);
    const domain = normalizeDomain(req.body.domain);
    const byEmail = new Map();

    for (const user of users) {
      const email = mailuUserEmail(user);
      if (!email || !email.endsWith(`@${domain}`)) continue;
      byEmail.set(email, [...(byEmail.get(email) || []), user]);
    }

    const duplicates = [...byEmail.entries()]
      .filter(([, entries]) => entries.length > 1)
      .map(([email, entries]) => ({
        email,
        count: entries.length,
        keep: mailuUserId(entries[0]),
        delete: entries.slice(1).map((entry) => mailuUserId(entry)),
      }));

    res.json({
      message: duplicates.length
        ? `Found ${duplicates.length} duplicate Mailu address group(s)`
        : "No duplicate Mailu addresses found",
      summary: {
        existingUsers: users.length,
        duplicateGroups: duplicates.length,
        duplicateUsersToDelete: duplicates.reduce(
          (total, duplicate) => total + duplicate.delete.length,
          0
        ),
      },
      duplicates,
    });
  } catch (error) {
    next(error);
  }
});

router.post("/cleanup-duplicates", async (req, res, next) => {
  try {
    validateMailuConfig(req.body);
    const dryRun = req.body.dryRun !== false;
    const users = await listMailuUsers(req.body);
    const domain = normalizeDomain(req.body.domain);
    const byEmail = new Map();

    for (const user of users) {
      const email = mailuUserEmail(user);
      if (!email || !email.endsWith(`@${domain}`)) continue;
      byEmail.set(email, [...(byEmail.get(email) || []), user]);
    }

    const deletionCandidates = [...byEmail.entries()].flatMap(([email, entries]) =>
      entries.slice(1).map((user) => ({ email, user, id: mailuUserId(user) }))
    );
    const results = [];
    const summary = {
      checked: users.length,
      duplicateUsersFound: deletionCandidates.length,
      deleted: 0,
      failed: 0,
      dryRun: dryRun ? 1 : 0,
    };

    for (const candidate of deletionCandidates) {
      if (dryRun) {
        results.push({
          email: candidate.email,
          id: candidate.id,
          status: "checked",
          message: "Would delete duplicate user",
        });
        continue;
      }

      const deleteResult = await deleteMailuUser(req.body, candidate.user);
      const status = deleteResult.ok ? "deleted" : "failed";
      summary[status] += 1;
      results.push({
        email: candidate.email,
        id: candidate.id,
        status,
        message:
          deleteResult.data.message ||
          (deleteResult.ok ? "Deleted duplicate user" : `Mailu returned HTTP ${deleteResult.status}`),
      });
    }

    res.json({
      message: dryRun
        ? `Dry run found ${summary.duplicateUsersFound} duplicate Mailu user(s) to delete`
        : `Mailu duplicate cleanup finished: ${summary.deleted} deleted, ${summary.failed} failed`,
      summary,
      results,
    });
  } catch (error) {
    next(error);
  }
});

router.post("/cleanup-generated-existing", async (req, res, next) => {
  try {
    validateMailuConfig(req.body);
    const domain = normalizeDomain(req.body.domain);
    const dryRun = req.body.dryRun !== false;
    const normalizedUsers = dedupeUsersByEmail(
      (Array.isArray(req.body.users) ? req.body.users : []).map((user) =>
        normalizeGeneratedUser(user, req.body.batchId || "cleanup", domain)
      )
    );

    if (normalizedUsers.length === 0) {
      const error = new Error("No generated users were provided for cleanup");
      error.statusCode = 400;
      throw error;
    }

    const requestedFirstNameKeys = new Set(
      normalizedUsers
        .map((user) => generatedEmailFirstNameKey(user.email, domain))
        .filter(Boolean)
    );
    const users = await listMailuUsers(req.body);
    const existingByFirstName = groupGeneratedMailuUsersByFirstName(users, domain);
    const requestedPatternUsers = [...requestedFirstNameKeys].flatMap(
      (firstNameKey) => existingByFirstName.get(firstNameKey) || []
    );
    const globalDuplicatePatternUsers = [...existingByFirstName.values()].flatMap(
      (entries) =>
        entries.length > 1
          ? entries.slice().sort((left, right) =>
              mailuUserEmail(left).localeCompare(mailuUserEmail(right))
            )
          : []
    );
    const existingGeneratedUsers = uniqueMailuUsers([
      ...requestedPatternUsers,
      ...globalDuplicatePatternUsers,
    ]);
    const results = [];
    const summary = {
      checked: normalizedUsers.length,
      firstNamePatternsChecked: requestedFirstNameKeys.size,
      duplicateFirstNamePatternsFound: [...existingByFirstName.values()].filter(
        (entries) => entries.length > 1
      ).length,
      existingGeneratedUsers: existingGeneratedUsers.length,
      protected: 0,
      deleted: 0,
      failed: 0,
      dryRun: dryRun ? 1 : 0,
    };

    for (const existing of existingGeneratedUsers) {
      const existingEmail = mailuUserEmail(existing);
      const firstNameKey = generatedEmailFirstNameKey(existingEmail, domain);

      if (isProtectedEmail(existingEmail)) {
        summary.protected += 1;
        results.push({
          email: existingEmail,
          id: mailuUserId(existing),
          status: "protected",
          message: "Skipped protected mailbox",
        });
        continue;
      }

      if (dryRun) {
        results.push({
          email: existingEmail,
          id: mailuUserId(existing),
          status: "checked",
          message: `Would delete generated mailbox matching first-name pattern ${firstNameKey}`,
        });
        continue;
      }

      const deleteResult = await deleteMailuUser(req.body, existing);
      const status = deleteResult.ok ? "deleted" : "failed";
      summary[status] += 1;
      results.push({
        email: existingEmail,
        id: mailuUserId(existing),
        status,
        message:
          deleteResult.data.message ||
          (deleteResult.ok
            ? `Deleted generated mailbox matching first-name pattern ${firstNameKey}`
            : `Mailu returned HTTP ${deleteResult.status}`),
      });
    }

    res.json({
      message: dryRun
        ? `Dry run found ${summary.existingGeneratedUsers} generated Mailu mailbox(es) from ${summary.firstNamePatternsChecked} requested and ${summary.duplicateFirstNamePatternsFound} duplicate first-name pattern(s)`
        : `Generated Mailu mailbox cleanup finished: ${summary.deleted} deleted, ${summary.failed} failed, ${summary.protected} protected`,
      summary,
      results,
    });
  } catch (error) {
    next(error);
  }
});

router.post("/create", async (req, res, next) => {
  try {
    validateMailuConfig(req.body);
    const domain = normalizeDomain(req.body.domain);
    const dryRun = Boolean(req.body.dryRun);
    const saved = await saveGeneratedBatch(
      req.body.users,
      domain,
      "mailu-create",
      req.body.batchId
    );
    const generatedDocs = await MailuGeneratedUser.find({
      batchId: saved.batchId,
      email: { $in: saved.users.map((user) => user.email) },
    }).lean();
    const generatedByEmail = new Map(
      generatedDocs.map((user) => [user.email, user])
    );
    const results = [];
    const summary = { created: 0, skipped: 0, failed: 0, checked: 0, existing: 0 };
    const existingUsers = await listMailuUsers(req.body);
    const existingEmails = new Set(existingUsers.map(mailuUserEmail).filter(Boolean));
    const existingFirstNameKeys = new Set(
      existingUsers
        .map((user) => generatedEmailFirstNameKey(mailuUserEmail(user), domain))
        .filter(Boolean)
    );

    for (const user of saved.users) {
      const generatedDoc = generatedByEmail.get(user.email);
      const firstNameKey = generatedEmailFirstNameKey(user.email, domain);
      const hasExistingPattern = firstNameKey && existingFirstNameKeys.has(firstNameKey);

      if (dryRun) {
        summary.checked += 1;
        if (existingEmails.has(user.email) || hasExistingPattern) summary.existing += 1;
        results.push({
          email: user.email,
          status: "checked",
          message: existingEmails.has(user.email)
            ? "Already exists in Mailu"
            : hasExistingPattern
              ? `Skipped because generated first-name pattern ${firstNameKey} already exists in Mailu`
              : "Ready to create",
        });
        continue;
      }

      let mailuResult;
      if (existingEmails.has(user.email) || hasExistingPattern) {
        mailuResult = {
          ok: false,
          status: 409,
          data: {
            message: existingEmails.has(user.email)
              ? "Skipped because user already exists in Mailu"
              : `Skipped because generated first-name pattern ${firstNameKey} already exists in Mailu`,
          },
        };
      } else {
        try {
          mailuResult = await callMailu(req.body, "/v1/user", {
            method: "POST",
            body: JSON.stringify(toMailuPayload(user)),
          });
        } catch (error) {
          mailuResult = {
            ok: false,
            status: 0,
            data: { message: error.message || "Mailu request failed" },
          };
        }
      }
      const status = mailuResult.ok
        ? "created"
        : mailuResult.status === 409
          ? "skipped"
          : "failed";

      summary[status] += 1;
      if (mailuResult.status === 409) summary.existing += 1;
      if (mailuResult.ok) {
        existingEmails.add(user.email);
        if (firstNameKey) existingFirstNameKeys.add(firstNameKey);
      }

      const result = {
        batchId: saved.batchId,
        generatedUserId: generatedDoc?._id,
        domain,
        email: user.email,
        password: user.password,
        displayedName: user.name || user.email.split("@")[0],
        status,
        message:
          mailuResult.data.message ||
          (mailuResult.ok ? "Created" : `Mailu returned HTTP ${mailuResult.status}`),
        dryRun,
        mailuStatusCode: mailuResult.status,
      };

      await MailuCreatedUser.findOneAndUpdate(
        { batchId: saved.batchId, email: user.email, dryRun },
        { $set: result },
        { new: true, upsert: true }
      );

      results.push({
        email: result.email,
        status: result.status,
        message: result.message,
      });
    }

    if (!dryRun) {
      await MailuGeneratedBatch.findOneAndUpdate(
        { batchId: saved.batchId },
        { $set: { latestMailuSummary: summary } }
      );
    }

    res.json({
      message: dryRun
        ? `Dry run checked ${summary.checked} generated users`
        : `Mailu batch finished: ${summary.created} created, ${summary.skipped} skipped, ${summary.failed} failed`,
      batchId: saved.batchId,
      summary,
      results,
    });
  } catch (error) {
    next(error);
  }
});

